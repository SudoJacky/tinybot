let currentLang = "zh";

function detectLanguage() {
  const stored = localStorage.getItem("tinybot-lang");
  if (stored && (stored === "zh" || stored === "en")) {
    return stored;
  }
  const browserLang = navigator.language || navigator.userLanguage;
  if (browserLang && browserLang.toLowerCase().startsWith("zh")) {
    return "zh";
  }
  return "en";
}

function t(key) {
  const trans = translations[currentLang];
  if (trans && trans[key]) {
    return trans[key];
  }
  // fallback to key
  return key;
}

function setLanguage(lang) {
  if (lang !== "zh" && lang !== "en") {
    lang = "zh";
  }
  currentLang = lang;
  localStorage.setItem("tinybot-lang", lang);
  applyTranslations();
  // dispatch event for app.js to update dynamic content
  window.dispatchEvent(new CustomEvent("languagechange", { detail: { lang } }));
}

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const text = t(key);
    el.textContent = text;
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const text = t(key);
    el.placeholder = text;
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const key = el.getAttribute("data-i18n-title");
    const text = t(key);
    el.title = text;
  });
  // Help tooltips for config fields
  document.querySelectorAll("[data-i18n-help]").forEach((el) => {
    const key = el.getAttribute("data-i18n-help");
    const text = t(key);
    el.setAttribute("data-help", text);
  });
}

function getLanguage() {
  return currentLang;
}

function initI18n() {
  currentLang = detectLanguage();
  applyTranslations();
}

// auto init when DOM ready
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initI18n);
} else {
  initI18n();
}
